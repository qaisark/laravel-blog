    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $room = hostel_room::find($id);
        $room->delete();
        return redirect('/hostel_room')->with('success','Room Deleted');
    }
